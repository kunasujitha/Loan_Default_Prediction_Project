import streamlit as st
import pickle
import pandas as pd
import numpy as np
import joblib

#Load your trained model
try:
  with open('loan_model.pkl','rb') as file:
    model = pickle.load(file)
except FileNotFoundError:
  st.error("Error: The 'loan_model.pkl' file was not found.Please make sure it's in the same directory.")
  st.stop()

# Set up the Streamlit app title and description
st.title("🏦 Loan Default Prediction App")
st.write("Enter the applicant's details to predict their loan approval status.")

#Create input fields for user data
st.header("Applicant Information")

#input fields
age = st.number_input('Age',min_value=18, max_value=100, value=30,key="age")
income = st.number_input('Annual Income',min_value=0,value=50000,key = 'income')
loan_amount = st.number_input('Loan Amount',min_value = 0,value = 20000,key="loan_amt")
credit_score = st.slider('Credit Score', 300, 850, 600)

months_employed = st.number_input('Months Employed',min_value=0,value = 12,key = 'months_emp')
num_credit_lines = st.number_input('No. of credit lines',min_value=0, value=2, key = 'num_credit_lines')
interest_rate = st.number_input("Interest Rate (%)", min_value=0.0, value=10.0, key="int_rate")
loan_term = st.selectbox('Loan Term (Months)',[12, 24, 36, 48, 60],key="loan_term")
dti_ratio = st.slider('Debt-to-Income Ratio',0.0,1.0,0.3)

education = st.selectbox('Education', ['High School', "Bachelor's", "Master's"])
employment = st.selectbox('Employment Type',['Full-time',"Part-time", 'Unemployes'])
marital_status = st.selectbox('Marital Status',['Single','Married','Divorced'])
has_mortgage = st.radio('Has Co-Signer?',['Yes','No'],key = 'has_mortgage')

has_dependents = st.radio("Has Dependents?", ["Yes", "No"],key = 'has_dependents')
loan_purpose = st.selectbox("Loan Purpose", ["Auto", "Business", "Education", "Other"])
has_cosigner = st.radio("Has Co-Signer?", ["Yes", "No"],key = 'has_cosigner')

#Convert categorical to numeric(simplified, must watch your preprocessing!)
has_mortgage = 1 if has_mortgage == 'Yes' else 0
has_dependents = 1 if has_dependents == 'Yes' else 0
has_cosigner = 1 if has_cosigner == 'Yes' else 0

#Prepare input array (must match your training feature order)
#features = np.array([age, income, loan_amount, credit_score, months_employed, num_credit_lines, interest_rate,
#                     loan_term, dti_ratio, has_mortgage, has_dependents, has_cosigner]).reshape(1,-1)

#Scale numerical features

#do preprocessing same as like u do  before training
#adjusting columns as per training dataset
#loading final_columns i.e.,you have trained the model with this set of columns(with order)
final_columns = joblib.load('final_columns.pkl')

#convert user inputs into dataframe
input_data = {'Age':age,
              'Income':income,
              'LoanAmount':loan_amount,
              'CreditScore':credit_score,
              'MonthsEmployed':months_employed,
              'NumCreditLines':num_credit_lines,
              'InterestRate':interest_rate,
              'LoanTerm':loan_term,
              'DTIRatio':dti_ratio,
              'Education':education,
              'EmploymentType':employment,
              'MaritalStatus':marital_status,
              'HasMortgage':has_mortgage,
              'HasDependents':has_dependents,
              'LoanPurpose':loan_purpose,
              'HasCoSigner':has_cosigner,
              }
input_df = pd.DataFrame([input_data])
input_df = pd.get_dummies(input_df)
input_df = input_df.reindex(columns = final_columns, fill_value =0)

#prediction = model_dec.predict(input_df)

#Prediction
if st.button("Predict"):
  prediction = model.predict(input_df)[0]
  if prediction == 1:
    st.error('⚠️ The applicant is likely to DEFAULT on the loan!')
  else:
    st.success('✅ The applicant is NOT likely to default.')

