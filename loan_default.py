#import libraries
import pandas as pd
from sklearn.linear_model import LogisticRegression
from sklearn.tree import DecisionTreeClassifier, plot_tree, export_text
from sklearn.ensemble import RandomForestClassifier
from sklearn.metrics import accuracy_score, precision_score, recall_score, classification_report,confusion_matrix
from sklearn.preprocessing import LabelEncoder, StandardScaler
from sklearn.model_selection import train_test_split
from imblearn.over_sampling import SMOTENC
import matplotlib.pyplot as plt
import seaborn as sns

#import data
df = pd.read_csv('Loan_default.csv')
df.head()

#Basic EDA
df.info()

df.isnull().sum()

df.describe()

#To check class imbalance
default_plot = df['Default'].value_counts()
default_plot

plt.figure(figsize=(6,4))
plt.bar(default_plot.index, default_plot.values,color =['green','red'])
plt.xticks([0,1])
plt.show()

#Data preprocessing
#drop irrelavent columns
df = df.drop(columns = ['LoanID'])

#Handle categorical varibles
categorical_columns = df.select_dtypes(include = ['object']).columns
le = LabelEncoder()
for col in categorical_columns:
  print(col,':',df[col].nunique())
  if df[col].nunique() == 2:        #Binary Category yes/no
    print('Binary categorical columns:',col)
    df[col] = le.fit_transform(df[col])
  else:
    print('categorical column with more classes:',col)
    df = pd.get_dummies(df, columns=[col],drop_first = True)

df.head()

#Handling Missing Values
df = df.fillna(df.median(numeric_only = True))

#Seperate features and target
X = df.drop(columns=['Default'])
y = df['Default']

#splitting training and testing sets
X_train,X_test,y_train,y_test = train_test_split(X, y,test_size = 0.3,stratify = y,random_state = 42)

#using the model Decisiontreeclassifier using class_weight='balanced'
model_dec = DecisionTreeClassifier(class_weight='balanced',random_state = 42)
model_dec.fit(X_train,y_train)

y_pred_dec = model_dec.predict(X_test)
print('Decision Regression using class_weight Model Evaluation')
print('accuracy_score:',accuracy_score(y_test,y_pred_dec))
print('precision_score:',precision_score(y_test,y_pred_dec))
print('recall_score:',recall_score(y_test,y_pred_dec))
print('confusion_matrix:\n',confusion_matrix(y_test,y_pred_dec))

#after dropping irrelavent columns
#categorical_columns = X_train.select_dtypes(include=['object']).columns

categorical_columns =["Education_High School",
       "Education_Master's", "Education_PhD",'EmploymentType_Part-time',
       'EmploymentType_Self-employed', 'EmploymentType_Unemployed','MaritalStatus_Married', 'MaritalStatus_Single','HasMortgage',
       'HasDependents', 'HasCoSigner', 'LoanPurpose_Business',
       'LoanPurpose_Education', 'LoanPurpose_Home', 'LoanPurpose_Other']
categorical_indices = [X_train.columns.get_loc(col) for col in categorical_columns]
categorical_indices

smote_nc = SMOTENC(categorical_features=categorical_indices, random_state=42)
X_train_smotenc,y_train_smotenc = smote_nc.fit_resample(X_train,y_train)

#Decisiontreeclassifier with smotenc
dec_clf_smotenc = DecisionTreeClassifier(random_state = 42)
dec_clf_smotenc.fit(X_train_smotenc,y_train_smotenc)
y_pred_dec_smotenc = dec_clf_smotenc.predict(X_test)
print('Decision tree classifier using smotenc Model Evaluation')
print('accuracy_score:',accuracy_score(y_test,y_pred_dec_smotenc))
print('precision_score:',precision_score(y_test,y_pred_dec_smotenc))
print('recall_score:',recall_score(y_test,y_pred_dec_smotenc))
print('confusion_matrix:\n',confusion_matrix(y_test,y_pred_dec_smotenc))

#using the model randomforestclassifier
model_rf = RandomForestClassifier(class_weight='balanced',n_estimators=100,max_depth=3,random_state=42)
model_rf.fit(X_train,y_train)

y_pred_rf = model_rf.predict(X_test)
print('Random Forest classifier using class weight model evaluation')
print('accuracy_score:',accuracy_score(y_test,y_pred_rf))
print('precision_score:',precision_score(y_test,y_pred_rf))
print('recall_score:',recall_score(y_test,y_pred_rf))
print('confusion_matrix:\n',confusion_matrix(y_test,y_pred_rf))

